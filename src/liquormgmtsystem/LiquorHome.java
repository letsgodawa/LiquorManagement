    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        
         Product p=new Product();
        boolean status=true; 
        
        if(tfName.getText().equals("")){
            JOptionPane.showMessageDialog(null,"please enter name","Failed",JOptionPane.WARNING_MESSAGE);
            status=false;
           
        }
        else{
            
            p.setName(tfName.getText());
        }
        
        if(tfDistributors.getText().equals("")){
            JOptionPane.showMessageDialog(null,"please enter Distributors number","Failed",JOptionPane.WARNING_MESSAGE);
         
        status=false;
           
       
    }                                        
        else{
             p.setDistributors((tfDistributors.getText()));
        }
        
        
        if(tfDate.getText().equals("")){
            
            JOptionPane.showMessageDialog(null,"please enter Date","Failed",JOptionPane.WARNING_MESSAGE);
             
            status=false;
            
        }
        else{
            DateTimeFormatter dateTimeFormatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            
           p.setDate(LocalDate.parse(tfDate.getText(),dateTimeFormatter));
            
        }
        if(tfQuantity.getText().equals("")){
            JOptionPane.showMessageDialog(null,"please enter quantity ","failed",JOptionPane.WARNING_MESSAGE);
        }else{
            p.setQuantity(Integer.parseInt(tfQuantity.getText()));
        }
        
        


        
            //perform databse operation if fields are filled up properly
            if(status){
                if(new ProductDao().addProduct(p)){
               
                    JOptionPane.showMessageDialog(null,"insert successfull","Success",JOptionPane.INFORMATION_MESSAGE);
                    show_customer();
                    
                }else{
                    JOptionPane.showMessageDialog(null,"Insert unsuccessful ","failed",JOptionPane.WARNING_MESSAGE);
                }
            }
        

        
        
        
    }//GEN-LAST:event_btnAddActionPerformed

