    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        
        String Driver="com.mysql.jdbc.Driver";
        String USER="root";
        String URL="jdbc:mysql://localhost:3306/liquor_db";
        String PASS="";
        Connection con=null;
        PreparedStatement ps=null;
        DateTimeFormatter dtf=DateTimeFormatter.ofPattern("yyyy-MM-dd");
        String dateValue=tfDate.getText();
        LocalDate v=LocalDate.parse(dateValue,dtf);
        int check=Integer.parseInt(tfQuantity.getText());
       String productName=tfName.getText();
       String distributorName=tfDistributors.getText();
        
        
        try{
            Class.forName(Driver);
            con=DriverManager.getConnection(URL, USER, PASS);
            int row=jTable1.getSelectedRow();
            String value=(jTable1.getModel().getValueAt(row,0).toString());
            String query="update liquor_tbl set name=?, distributors=?, date=?, quantity=? where id="+value;
            ps=con.prepareStatement(query);
            ps.setString(1,tfName.getText());
            ps.setString(2,tfDistributors.getText());
            ps.setDate(3, Date.valueOf(v));
            ps.setInt(4, Integer.parseInt(tfQuantity.getText()));
            ps.executeUpdate();
            
            DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
            show_customer();
            JOptionPane.showMessageDialog(null,"Update Successfull","success",JOptionPane.WARNING_MESSAGE);
            if(check<=20){
                JOptionPane.showMessageDialog(null,productName+" quantity is low contact "+distributorName,"Notification",JOptionPane.WARNING_MESSAGE);
            }
            
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
        try{
        if(con!=null) con.close();
        if(ps!=null) ps.close();
    }catch(SQLException ex){
        System.out.println(ex);
    }
        }   
    }//GEN-LAST:event_btnEditActionPerformed

